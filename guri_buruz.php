    <p>Hasieratik gure filosofia oso argia izan da, lehenik eta behin ingurumen hezkuntza sustatzen dugu jarduera ezberdinen bitartez, hala nola, eskola ezberdinei hitzaldiak eta ateraldiak eskaintzen, baita ere tailer ezberdinak egiten.
